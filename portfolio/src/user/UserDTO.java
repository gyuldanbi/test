package user;

public class UserDTO {
	private String id;
	private String password;
	private String name;
	private String sex;
	private String birth;
	private String number;
	private String email;
	private String postcode;
	private String address;
	private String send_mail;
	private String send_text;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getSend_mail() {
		return send_mail;
	}
	public void setSend_mail(String send_mail) {
		this.send_mail = send_mail;
	}
	public String getSend_text() {
		return send_text;
	}
	public void setSend_text(String send_text) {
		this.send_text = send_text;
	}
}
